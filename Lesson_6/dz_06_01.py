# Сворюю лічильник спроб і зберігаю в змінну count
# Створюю цикл while який працює 7 разів( 6 разів для спроб і один для вивода повідомлення про програш)
# В середені цикла запрошуємо число від користувача і присваюваємо значення в змінну number
# Потім визиваємо функцію перевірки номера number_check і вже порівнюємо вивід і виводимо відповідне повідомлення
# Як що користувач вгадав - цикл зупиняється
# Як що ні - цикл працює до кінця і виводить повідомлення про програш
